    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
             String   username =  jTextField1.getText();
             String password = String.valueOf(jPasswordField1.getPassword());
             
             if(username.isEmpty()){
                 JOptionPane.showMessageDialog(this, "Please Enter Your Username to Remove", "Alert", JOptionPane.ERROR_MESSAGE);
                 jTextField1.grabFocus();
             }else if(password.isEmpty()){
                 JOptionPane.showMessageDialog(this, "Enter Your Password to Continue !", "Alert", JOptionPane.ERROR_MESSAGE);
                 jPasswordField1.grabFocus();
             }else{
                 
                 try {
                     
                  ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `admin` WHERE `username`= '"+username+"' AND `password`='"+password+"' ");
                     
                   if(resultSet.next()){
                       
                     Integer Delete = MySQL.executeIUD("DELETE FROM `admin` WHERE `username`= '"+username+"' AND `password`='"+password+"' ");
                     Adminload();
                     jTextField1.setText("");
                     jPasswordField1.setText("");
                     JOptionPane.showMessageDialog(this, "Successfully Removed !", "Success", JOptionPane.PLAIN_MESSAGE);
                      
                     
                   }else{
                       JOptionPane.showMessageDialog(this, "Invalid Username or Password !", "Alert", JOptionPane.ERROR_MESSAGE);
                   }
                     
                     
                 } catch (Exception e) {
                   JOptionPane.showMessageDialog(this, "Internal Error Appeared Please Try Again Error"+e, "Error", JOptionPane.ERROR_MESSAGE);
                 }
                 
                 
             }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

