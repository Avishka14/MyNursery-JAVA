    private void Adminload(){
        
        try {
            
            ResultSet admins = MySQL.executeSearch("SELECT * FROM `admin`");
            
             DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
              dtm.setRowCount(0);
            
            while(admins.next()){
                 Vector vector = new Vector();
                 vector.add(admins.getString("name"));
                 dtm.addRow(vector);                               
            }
            
            jTable1.setModel(dtm);
                           
        } catch (Exception e) {
       JOptionPane.showMessageDialog(this, "Internal Error Appeared Please Try Again Error"+e, "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }
    
    
    
    

